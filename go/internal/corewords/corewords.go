// Package corewords содержит встроенные ("ядро") наборы триггер-слов —
// готовые категории, которые администратор конкретного чата может одной
// командой (/addcorewords <категория>, см. cmd/bot/admin.go) добавить в
// свой per-chat список фильтра слов, вместо того чтобы вводить каждое слово
// вручную через /addspam.
//
// Это ядро неизменно и правится только здесь, в коде (перекомпиляция и
// деплой — как и было задумано: "базовые слова, которые всегда одни и те
// же" против "то, что админ добавляет сам"). Хранилище для
// админ-добавленных слов — существующая таблица chat_triggers в
// PostgreSQL (internal/storage), отдельного файла/хранилища для ядра не
// заводим.
//
// Ядро применяется только по явной команде конкретного чата, а не
// автоматически для всех чатов, где стоит бот: фильтрация мата — это
// решение о политике модерации, которое должен принять админ каждого чата
// сам, а не то, что бот навязывает всем по умолчанию.
//
// Слова, которые часто ложно совпадают как подстрока обычных слов
// (фильтр matчит по вхождению — см. internal/storage.MatchTrigger), из
// ядра исключены. Например "лох" был бы найден внутри "плохой"/"неплохо".
// "ставки" (категория spam) может ложно сработать внутри "поставки"/
// "доставки" — сознательно оставлено, т.к. это не короткое слово с
// очевидной коллизией, но стоит иметь в виду при включении этой категории.
package corewords

